package com.itheima.test;

public class Test12 {
    public static void main(String[] args) {
        //需求1：打印1-5
        System.out.println("开始打印1-5：");
        for (int i = 1; i <= 5; i++){
            System.out.println(i);
        }
        //需求2：打印5-1
        System.out.println("开始打印5-1：");
        for (int j = 5; j >= 1; j--){
            System.out.println(j);
        }
    }
}
